# ------------------------------------------------------------
# Python 的集合（set）特性：
#   1. 內部元素不重複（唯一）
#   2. 無序（沒有固定順序）
#   3. 可使用 { } 或 set() 建立
# ------------------------------------------------------------

# 注意：{ } 在 Python 裡預設是「空字典」，不是空集合！
empty_dict = {}   # 建立空字典
print("列印類別 =", type(empty_dict))   # <class 'dict'>

# 建立空集合一定要使用 set()
empty_set = set()  # 正確的空集合建立方式
print("列印類別 =", type(empty_set))   # <class 'set'>

# ------------------------------------------------------------
# Python 集合（set）運算符號對照表
# ------------------------------------------------------------

# &   ：交集（Intersection）
#        取得兩個集合中「共同存在」的元素
#        A & B

# |   ：聯集（Union）
#        取得兩個集合所有元素（不重複）
#        A | B

# -   ：差集（Difference）
#        取得「在 A 中，但不在 B 中」的元素
#        A - B

# ^   ：對稱差集（Symmetric Difference）
#        取得「只存在於其中一個集合，但不在兩者交集內的元素」
#        A ^ B

# ==  ：等於（Equality）
#        判斷兩個集合是否擁有「完全相同的元素」

# !=  ：不等於（Not Equal）
#        判斷兩個集合是否「不完全相同」

# in      ：成員運算（Is Member）
#            判斷元素是否存在於集合中
#            x in A  → True / False

# not in  ：非成員運算（Not Member）
#            判斷元素是否「不存在於集合中」
#            x not in A
# ------------------------------------------------------------
# ------------------------------------------------------------
# Python 集合（set）常用方法
# ------------------------------------------------------------

# add(x)
#   將元素 x 加入集合（若 x 已存在，集合不會改變）
#   用法：A.add(5)
#
# clear()
#   刪除集合內所有元素，變成空集合
#   用法：A.clear()
#
# copy()
#   回傳集合的淺拷貝（新集合，但內容相同）
#   用法：B = A.copy()
#
# difference_update(other_set)
#   從集合中移除「與 other_set 重複的元素」
#   等同：A = A - other_set，但 difference_update 是修改原集合
#   用法：A.difference_update(B)
#
# discard(x)
#   若 x 在集合中 → 刪除 x
#   若 x 不存在 → 不會報錯
#   用法：A.discard(3)
#
# intersection_update(other_set)
#   使用交集更新集合內容，只保留兩集合共同的元素
#   用法：A.intersection_update(B)
#
# isdisjoint(other_set)
#   若兩集合「沒有任何交集」，回傳 True
#   若有共同元素，回傳 False
#   用法：A.isdisjoint(B)
#
# issubset(other_set)
#   若 A 是 B 的子集合（A 的所有元素都在 B），回傳 True
#   用法：A.issubset(B)
#
# issuperset(other_set)
#   若 A 是 B 的父集合（A 包含 B 所有元素），回傳 True
#   用法：A.issuperset(B)
#
# pop()
#   隨機刪除並回傳集合中的一個元素（集合是無序的）
#   若集合為空會產生 KeyError
#   用法：A.pop()
#
# remove(x)
#   刪除集合中的元素 x
#   若 x 不存在 → 產生 KeyError
#   用法：A.remove(3)
#
# symmetric_difference_update(other_set)
#   使用「對稱差集」更新集合內容
#   對稱差集＝兩集合中不重複的元素（不在交集的那些）
#   用法：A.symmetric_difference_update(B)
#
# update(other_set,...)
#   使用聯集更新集合內容（加入其他集合的元素）
#   等同：A = A ∪ B
#   用法：A.update(B)
# ------------------------------------------------------------
# ------------------------------------------------------------
# frozenset（凍結集合）
# ------------------------------------------------------------
# 1. set 是可變集合（mutable），可以新增或刪除元素。
# 2. frozenset 是不可變集合（immutable），建立後不能再修改內容。
# 3. 類似概念：
#       tuple 是不可變的 list
#       frozenset 就是不可變的 set
#
# 4. frozenset 不能使用 add()、remove() 等修改方法，
#    但可以做集合運算：交集、聯集、差集、對稱差集。
# 5. 因為它不可變，因此可以當字典的 key，也可以放入 set 中。
# ------------------------------------------------------------

# 建立兩個凍結集合
X = frozenset([1, 3, 5])  # frozenset 的內容不可更改
Y = frozenset([5, 7, 9])

# 印出凍結集合
print(X)  # frozenset({1, 3, 5})
print(Y)  # frozenset({5, 7, 9})

# ------------------------------------------------------------
# 集合運算：frozenset 依然支援 &（交集）、|（聯集）
# ------------------------------------------------------------

# 交集（共同的元素）
print("交集 = ", X & Y)     # frozenset({5})

# 聯集（全部元素，不重複）
print("聯集 = ", X | Y)     # frozenset({1, 3, 5, 7, 9})

# ------------------------------------------------------------
# 集合運算可以存入變數 A，結果仍然是 frozenset
# ------------------------------------------------------------

# 交集（使用 & 運算子）
A = X & Y
print("交集A = ", A)        # frozenset({5})

# 交集（使用 intersection 方法）
A = X.intersection(Y)
print("交集A = ", A)        # frozenset({5})

