# eval(expression)
# 功能：把「字串型算式」當作 Python 程式執行並回傳結果
# ------------------------------------------------------------
# 範例：
# expression = "3 + 5 * 2"
# result = eval(expression)
# print(result)  → 13
# ------------------------------------------------------------
# ch4_25.py
numberStr = input("請輸入數值公式 : ")  # 例：10 + 5 * 3
number = eval(numberStr)                # 轉成算式並計算
print(f"計算結果 : {number:5.2f}")       # 顯示結果，保留2位小數
# ------------------------------------------------------------
# 輸入：10 + 5 * 3
# 輸出：計算結果 : 25.00
# ------------------------------------------------------------
# 注意：
# eval() 可執行任意程式碼，實務上要避免直接使用使用者輸入。
# ------------------------------------------------------------
# 練習：使用 input() 與 eval() 輸入多個數字
# 使用者輸入時，各數字間要用逗號(,)隔開
# 因為 eval("80,90,70") 會被解讀為 (80,90,70)
# eval("80,90,70") 會把字串當成 Python 程式執行
# 在 Python 中，"80,90,70" 是一個 tuple (80, 90, 70)
# ------------------------------------------------------------
print("歡迎使用成績輸入系統")
name = input("請輸入姓名：")
scores = input("請輸入三個成績（以逗號分隔）：")  # 例：80,90,70
engh, math, sci = eval(scores)  # 將字串轉成三個數字,這是一種「多重指定 (multiple assignment)」
avg = (engh + math + sci) / 3
print(f"{name} 你的平均分數是 {avg:.2f}")
# ------------------------------------------------------------
# 若輸入 80,90,70 → 輸出 Mark 你的平均分數是 80.00
