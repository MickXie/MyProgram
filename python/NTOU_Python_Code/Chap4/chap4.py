# 格式化輸出資料使用 print() - 使用 format() 方法
# ------------------------------------------------------------
# 基本語法：
# print("…輸出格式區…".format(變數系列區, …))
# ------------------------------------------------------------
# format() 是 Python 中比 % 更現代、靈活的字串格式化方式。
# 可以在字串中使用 {} 作為「插入點」，再由 format() 將變數依序填入。
# ------------------------------------------------------------
# 範例 1（舊式格式化）：
score = 90
name = "Mark"
count = 1
print("%s你的第 %d 次物理考試成績是 %d" % (name, count, score))
# 說明：
# %s → 插入字串
# %d → 插入整數
# 三個 % 依序對應 (name, count, score)
# 輸出：Mark你的第 1 次物理考試成績是 90
# ------------------------------------------------------------
# 範例 2（新式 format()）：
score = 90
name = "Mark"
count = 1
print("{}你的第 {} 次物理考試成績是 {}".format(name, count, score))
# 說明：
# {} 是插入點，依序由 .format() 填入變數
# 第一個 {} → name
# 第二個 {} → count
# 第三個 {} → score
# 輸出：Mark你的第 1 次物理考試成績是 90
# ------------------------------------------------------------
# 進階用法：
# {0}、{1} 可指定位置（可重複使用）
# {name} 可使用命名方式填入變數
# {變數:格式控制} 可控制寬度、小數位、對齊等
# ------------------------------------------------------------
# 例：
# print("{0}的分數是{1}".format("Mark", 90))
# print("{name}你的第{count}次成績是{score}".format(name="Mark", count=1, score=90))
# print("{:>8}".format("Hi"))   → 右對齊
# print("{:<8}".format("Hi"))   → 左對齊
# print("{:^8}".format("Hi"))   → 置中
# print("{:.2f}".format(3.14159)) → 顯示兩位小數：3.14
# ------------------------------------------------------------
# format() 比 % 更安全與彈性，Python 3 推薦使用。
# ------------------------------------------------------------
print("{:^08}".format("Hi"))
name = "Mark"
count = 1
score = 90
print("{name}你的第 {count:02d} 次物理考試成績是 {score:08f}".format(
    name=name, count=count, score=score))
